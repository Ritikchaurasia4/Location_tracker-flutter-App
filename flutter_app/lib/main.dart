  Widget _createDrawerItem({
    required IconData icon,
    required String text,
    bool selected = false,
    VoidCallback? onTap,
  }) {
    return ListTile(
      leading: Icon(icon, color: selected ? Colors.blue : null),
      title: Text(
        text,
        style: TextStyle(
          color: selected ? Colors.blue : null,
        ),
      ),
      onTap: onTap,
    );
  }
}
