                  ),
                ],
              ),
            ),

            _createDrawerItem(
              icon: Icons.timer,
              text: 'Timer',
              onTap: () {
                // Handle Timer navigation if needed
              },
            ),
            _createDrawerItem(
              icon: Icons.check_circle_outline,
              text: 'Attendance',
              selected: true,
              onTap: () {
                Navigator.push(
                  context,
                  MaterialPageRoute(builder: (context) => AttendancePage()),
